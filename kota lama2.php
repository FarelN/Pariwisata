        <h3>KOTA LAMA</h3>
        <p>Diawali dari penandatangan perjanjian antara Kerajaan Mataram dan VOC pada 15 Januari 1678. Kala itu Amangkurat II menyerahkan Semarang kepada pihak VOC sebagai pembayaran karena VOC telah berhasil membantu Mataram menumpas pemberontakan Trunojoyo. Setelah Semarang berada di bawah kekuasaan penuh VOC, kota itu pun mulai dibangun. Sebuah benteng bernama Vijfhoek yang digunakan sebagai tempat tinggal warga Belanda dan pusat militer mulai dibangun. Lama kelamaan benteng tidak mencukupi, sehingga warga mulai membangun rumah di luar sebelah timur benteng. Tak hanya rumah-rumah warga, gedung pemerintahan dan perkantoran juga didirikan.<br>

